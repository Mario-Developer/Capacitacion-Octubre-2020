--1.- MOSTRAR LOS DETALLES DE MARCA, COLOR, TIPO.
    SELECT ID, MODELO, TALLA,
    (
        CASE MARCA
        WHEN 1 THEN 'HUSHPUPPI'
        WHEN 2 THEN 'NIKE'
        WHEN 3 THEN 'ADIDAS'
        WHEN 4 THEN 'TOMMY HILFIGER'
        ELSE 'NO ESTA'
        END
    ) AS MARCA,
    (
        CASE COLOR
        WHEN 1 THEN 'NEGRO'
        WHEN 2 THEN 'BLANCO'
        WHEN 3 THEN 'CAFE'
        WHEN 4 THEN 'AZUL'
        ELSE 'NO ESTA'
        END
    )AS COLOR,
    (
        CASE TIPO
        WHEN 1 THEN 'FORMAL'
        WHEN 2 THEN 'DEPORTIVO'
        WHEN 3 THEN 'CASUAL'
        ELSE 'NO ESTA'
        END
    )AS TIPO
    FROM ZAPATO;

--2.- BUSCAR LOS REGISTROS QUE SU COLOR SEA AZUL O NEGRO.
    SELECT ID, MODELO, TALLA,
    (
        CASE MARCA
        WHEN 1 THEN 'HUSHPUPPI'
        WHEN 2 THEN 'NIKE'
        WHEN 3 THEN 'ADIDAS'
        WHEN 4 THEN 'TOMMY HILFIGER'
        ELSE 'NO ESTA'
        END
    ),
    (
        CASE COLOR
        WHEN 1 THEN 'NEGRO'
        WHEN 2 THEN 'BLANCO'
        WHEN 3 THEN 'CAFE'
        WHEN 4 THEN 'AZUL'
        ELSE 'NO ESTA'
        END
    ),
    (
        CASE TIPO
        WHEN 1 THEN 'FORMAL'
        WHEN 2 THEN 'DEPORTIVO'
        WHEN 3 THEN 'CASUAL'
        ELSE 'NO ESTA'
        END
    )FROM ZAPATO WHERE COLOR IN (1,4);

--3.- BUSCAR EL ZAPATO MAS CARO .
SELECT ID, MODELO, TALLA,
(
    CASE MARCA
    WHEN 1 THEN 'HUSHPUPPI'
    WHEN 2 THEN 'NIKE'
    WHEN 3 THEN 'ADIDAS'
    WHEN 4 THEN 'TOMMY HILFIGER'
    ELSE 'NO ESTA'
    END
)AS MARCA,
(
    CASE COLOR
    WHEN 1 THEN 'NEGRO'
    WHEN 2 THEN 'BLANCO'
    WHEN 3 THEN 'CAFE'
    WHEN 4 THEN 'AZUL'
    ELSE 'NO ESTA'
    END
)AS COLOR,
(
    CASE TIPO
    WHEN 1 THEN 'FORMAL'
    WHEN 2 THEN 'DEPORTIVO'
    WHEN 3 THEN 'CASUAL'
    ELSE 'NO ESTA'
    END
)AS TIPO, PRECIO, TIPO
FROM ZAPATO WHERE PRECIO = (SELECT MAX(PRECIO) ZAPATO_CARO FROM ZAPATO);

--4.- BUSCAR LOS ZAPATOS QUE SU MODELO SEA TIPO R.
SELECT ID, MODELO, TALLA,
(
    CASE MARCA
    WHEN 1 THEN 'HUSHPUPPI'
    WHEN 2 THEN 'NIKE'
    WHEN 3 THEN 'ADIDAS'
    WHEN 4 THEN 'TOMMY HILFIGER'
    ELSE 'NO ESTA'
    END
),
(
    CASE COLOR
    WHEN 1 THEN 'NEGRO'
    WHEN 2 THEN 'BLANCO'
    WHEN 3 THEN 'CAFE'
    WHEN 4 THEN 'AZUL'
    ELSE 'NO ESTA'
    END
),
(
    CASE TIPO
    WHEN 1 THEN 'FORMAL'
    WHEN 2 THEN 'DEPORTIVO'
    WHEN 3 THEN 'CASUAL'
    ELSE 'NO ESTA'
    END
)
FROM ZAPATO WHERE MODELO LIKE 'R%';

--5.- CONTAR CUANTOS ZAPATOS HAY DE CADA COLOR.
    SELECT 
    (
        CASE COLOR
        WHEN 1 THEN 'NEGRO'
        WHEN 2 THEN 'BLANCO'
        WHEN 3 THEN 'CAFE'
        WHEN 4 THEN 'AZUL'
        ELSE 'NO ESTA'
        END
    )AS COLOR,
    COUNT (COLOR) AS CANTIDAD
    FROM ZAPATO
    GROUP BY (COLOR);
