CREATE TABLE ZAPATO(
    ID NUMBER PRIMARY KEY,
    MODELO NVARCHAR2(30),
    TALLA NUMBER,
    MARCA NUMBER,
    COLOR NUMBER,
    PRECIO NUMBER,
    TIPO NUMBER
);
