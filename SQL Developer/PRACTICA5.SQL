--1 MOSTRAR LOS AUTOS Y SU DUENO
SELECT *
FROM VEHICULO
INNER JOIN DUENO
ON DUENO.ID = VEHICULO.ID_DUENO

--2 CONTAR CUANTOS AUTOS TIENE CADA DUENO                    -->EJEMPLO IMPORTANTE
SELECT DUENO.NOMBRE, COUNT (ID_DUENO) AS CANTIDAD
FROM VEHICULO
INNER JOIN DUENO ON DUENO.ID = VEHICULO.ID_DUENO
GROUP BY (DUENO.NOMBRE);

--3 BUSCAR EL AUTO MAS CARO
SELECT * FROM VEHICULO
INNER JOIN DUENO ON DUENO.ID = VEHICULO.ID_DUENO
WHERE PRECIO = (SELECT MAX(PRECIO) FROM VEHICULO)

--4 BUSCAR EL AUTO QUE SU MODELO TENGA LA LETRA "A"
SELECT * FROM VEHICULO
INNER JOIN DUENO ON DUENO.ID = VEHICULO.ID_DUENO
WHERE MODELO LIKE '%A%';

--5 MOSTRAR LOS AUTOS QUE SU PRECIO ESTRE ENTRE 140000 Y 200000
SELECT * FROM VEHICULO
INNER JOIN DUENO ON DUENO.ID = VEHICULO.ID_DUENO
WHERE VEHICULO.PRECIO BETWEEN 140000 AND 200000;